                       pst.setString(4, txtaadharnumber.getText());
                       pst.setString(5, txtnatinonality.getText());
                       pst.setString(6, txtdate.getText());
                       pst.setString(7, jComboroomnumber.getItemAt(jComboroomnumber.getSelectedIndex()));
                       pst.setString(8, jComboroomtype.getItemAt(jComboroomtype.getSelectedIndex()));
                       pst.setString(9, jCombobedtype.getItemAt(jCombobedtype.getSelectedIndex()));
                       pst.setString(10, txtprice.getText());
                       pst.setString(11, "NULL");
                       pst.executeUpdate();
                       JOptionPane.showMessageDialog(this,"Customer Checked In Successfullyy ");
                       txtname.setText("");
        txtaadharnumber.setText("");
        txtmobileno.setText("");
        txtnatinonality.setText("");
        pst=con.prepareStatement("update room set status=? where roomnumber=?");
        pst.setString(1,"BOOKED");
        pst.setString(2,jComboroomnumber.getItemAt(jComboroomnumber.getSelectedIndex()));
        pst.executeUpdate();
