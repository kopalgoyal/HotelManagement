    public void Customer(){
        try {
            pst=con.prepareStatement("Select roomnumber from room where bedtype=? and roomtype=? and status=?");
            pst.setString(1,jCombobedtype.getItemAt(jCombobedtype.getSelectedIndex()));
            pst.setString(2,jComboroomtype.getItemAt(jComboroomtype.getSelectedIndex()));
            pst.setString(3,"NOT-BOOKED");
            rs=pst.executeQuery();
            jComboroomnumber.removeAllItems();
            while(rs.next()){
               jComboroomnumber.addItem(rs.getString("roomnumber"));
            }
               pst=con.prepareStatement("select price from room where roomnumber=?");
               pst.setString(1,jComboroomnumber.getItemAt(jComboroomnumber.getSelectedIndex()));
               rs=pst.executeQuery();
               if(rs.next()){
                   txtprice.setText(rs.getString("price"));
               }
            
        } catch (SQLException ex) {
            Logger.getLogger(CustomerCheckin.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

